# Dictionary methods in python:

# 1. clear() - Removes all the elements from the dictionary
# 2. copy() - Returns a copy of the dictionary
# 3. fromkeys() - Returns a dictionary with the specified keys and values
# 4. get() - Returns the value of the specified key
# 5. items() - Returns a list containing a tuple for each key-value pair in the dictionary
# 6. keys() - Returns a list containing the dictionary's keys
# 7. pop() - Removes the element with the specified key and returns its value
# 8. popitem() - Removes the last inserted key-value pair and returns it as a tuple
# 9. setdefault() - Returns the value of the specified key. If the key does not exist, it inserts the key with a specified value and returns that value
# 10. update() - Updates the dictionary with the specified key-value pairs
# 11. values() - Returns a list of all the values in the dictionary

# 12 . del - Deletes the entire dictionary or a specific key-value pair from the dictionary
# 13. in - Checks if a key exists in the dictionary
# 14. len() - Returns the number of key-value pairs in the dictionary
# 15. dict() - Creates a new dictionary
# 16. enumerate() - Returns an enumerate object that contains the index and value of each item in the dictionary
# 17. zip() - Returns an iterator of tuples, where the i-th tuple contains the i-th element from each of the argument sequences or iterables
# 18. sorted() - Returns a sorted list of the dictionary's keys or values
# 19. any() - Returns True if any key-value pair in the dictionary is true, otherwise returns False
# 20. all() - Returns True if all key-value pairs in the dictionary are true, otherwise returns False
# 21. isinstance() - Checks if an object is an instance of a specified class or a subclass thereof
# 22. type() - Returns the type of an object
# 23. id() - Returns the identity of an object
# 24. hash() - Returns the hash value of an object
# 25. help() - Invokes the built-in help system to provide information about a specified object or topic
